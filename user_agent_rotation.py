class user_agent_gen:
        
    def rand_headers(self):
        """Function which returns request headers with random user-agents scraped everytime the class is initialised """
        
        source=requests.get("https://developers.whatismybrowser.com/useragents/explore/software_name/chrome/")
        page=BeautifulSoup(source.text, "html.parser")
        user_agents=page.find_all("td",{"class":"useragent"})
        for i,agent in enumerate(user_agents):
            user_agents[i]=user_agents[i].text

        headers={   'Accept': 'application/json, text/javascript, */*; q=0.01',
                    'Accept-Encoding': 'gzip, deflate, br',
                    'Accept-Language': 'en-US,en;q=0.9',
                    'Connection': 'keep-alive',
                    'Content-Type': 'application/json;charset=utf-8',
                    'Host': 'www.shaw.sg',
                    'Sec-Fetch-Mode': 'cors',
                    'Sec-Fetch-Site': 'same-origin',
                    'User-Agent': random.choice(user_agents),
                    'X-Requested-With': 'XMLHttpRequest'
                }
        return headers

    def back_up_headers(self):
        """Function to generate random request headers from a pre-made list in case the rand_headers function 
           is unable to scrape user_agents due to sites which block scraping
        """
        default_headers={'Accept': 'application/json, text/javascript, */*; q=0.01',
            'Accept-Encoding': 'gzip, deflate, br',
            'Accept-Language': 'en-US,en;q=0.9',
            'Connection': 'keep-alive',
            'Content-Type': 'application/json;charset=utf-8',
            'Host': 'www.shaw.sg',
            'Sec-Fetch-Mode': 'cors',
            'Sec-Fetch-Site': 'same-origin',
            'User-Agent': random.choice(user_agent_list),
            'X-Requested-With': 'XMLHttpRequest'
            }
        return default_headers       

